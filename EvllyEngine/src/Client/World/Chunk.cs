    public class MarchingData
    {
        public List<float> _vertices;
        public List<float> _UVs;
        public List<int> _triangles;
        public List<float> _colors;

        public MarchingData(Vector3 chunkposition)
        {
            _vertices = new List<float>();
            _triangles = new List<int>();

            MarchingCubes marching = new MarchingCubes();

            marching.Surface = 0.0f;

            //The size of voxel array.
            int width = 17;
            int height = 17;
            int length = 17;

            float[] voxels = new float[width * height * length];

            //Fill voxels with values. Im using perlin noise but any method to create voxels will work.
            for (int x = 0; x < width; x++)
            {
                for (int y = 0; y < height; y++)
                {
                    for (int z = 0; z < length; z++)
                    {
                        float fx = x / (width - 1.0f);
                        float fy = y / (height - 1.0f);
                        float fz = z / (length - 1.0f);

                        int idx = x + y * width + z * width * height;

                        voxels[idx] = -(y + (int)chunkposition.Y) - MidleWorld.globalNoise.GetPerlin(x + (int)chunkposition.X, y + (int)chunkposition.Y, z + (int)chunkposition.Z) * 50;
                    }
                }
            }

            List<Vector3> verts = new List<Vector3>();

            //The mesh produced is not optimal. There is one vert for each index.
            //Would need to weld vertices for better quality mesh.
            marching.Generate(voxels, width, height, length, verts, _triangles);

            for (int i = 0; i < verts.Count; i++)
            {
                _vertices.Add(verts[i].X);
                _vertices.Add(verts[i].Y);
                _vertices.Add(verts[i].Z);
            }
        }
    }

