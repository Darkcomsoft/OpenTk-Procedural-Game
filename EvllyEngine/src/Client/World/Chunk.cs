                            Vector3 corner = new Vector3(x, y, z) + CornerTable[i];
                            cube[i] = terrainMap[(int)corner.X, (int)corner.Y, (int)corner.Z];

                        }

                        // Pass the value into our MarchCube function.
                        MarchCube(new Vector3(x, y, z), cube);

                    }
                }
            }
        }

